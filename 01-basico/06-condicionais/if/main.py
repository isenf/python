x = int(input("Digite um número: "))

# comparador de igualdade: ==
if(x == 5):
    print("x é igual a 5")

# comparador de desigualdade: >, <, >=, <=
if(x > 0):
    print("Numero positivo")

# comparador de diferença: !=
if(x != 0):
    print("x não é igual a zero")

# operadores lógicos: and, or, not
if(x == 10 and x > 0):
    print("x é igual a 10 e maior que zero")