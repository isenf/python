# inteiro
a = 5
b = 7
print(a, b, a + b, sep='\t') # operações básicas: +, -, *, /

# float (ponto flutuante)
x = 0.9
y = 0.5
print(x, y, x/y, sep='\t')

# undescores
num_grande = 145_000_000
print(num_grande)

# atribuição multipla
l, m, n = 8, 9.5, 4
print(l, m, n, sep='\t')

# não há um tipo de constante embutido no python, a convenção é declarar em maiúsculo
MIN_IDADE = 18
print(MIN_IDADE)
